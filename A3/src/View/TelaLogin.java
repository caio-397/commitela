    private boolean validaLogin() {
        String username = this.nome.getText();
        String password = this.senha.getText();
        try {
            if ("a".equals(username) && "a".equals(password)) {
                JOptionPane.showMessageDialog(rootPane, "Bem vindo " + username + "!");
                return true;
            } else {
                throw new Mensagens("Usu√°rio ou senha incorretos!");
            }
        } catch(Mensagens erro) {
            JOptionPane.showMessageDialog(rootPane, erro.getMessage());
            return false;
        }
        
    }
    
